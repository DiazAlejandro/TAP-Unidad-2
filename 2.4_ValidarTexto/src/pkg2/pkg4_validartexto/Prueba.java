public void keyTyped(KeyEvent e){
    int limite = Integer.parseInt(cantidad.getValue()) ;
        for (int i = limite ; i <= texto.getTextLength() ; i--)
        restan.setText("Faltan: " + (limite - texto.getTextLength()) + " Caracteres.");
        restan.setText(""+limite);
        e.consume();
} 
